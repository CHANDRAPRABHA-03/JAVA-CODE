class Chocolate {
    String brand;
    String flavor;
    double weight;
    boolean isOrganic;
    boolean isSugarFree;
    boolean hasNuts;
    double price;
    String packaging;
    String expiryDate;
    String countryOfOrigin;
    int cocoaPercentage;
    String color;
    boolean isGlutenFree;
    String shape;
    boolean isVegan;
    boolean isDairyFree;
    String taste;
    int calories;
    String storageInstructions;
    String ingredients;
    String manufacturingCompany;

public Chocolate(String brand, String flavor, double weight, boolean isOrganic, boolean isSugarFree, boolean      hasNuts, double price, String packaging, String expiryDate, String countryOfOrigin, int cocoaPercentage,      String color,boolean isGlutenFree, String shape, boolean isVegan, boolean isDairyFree, String taste, int      calories, String storageInstructions, String ingredients, String manufacturingCompany) {
	
        this.brand = brand;
        this.flavor = flavor;
        this.weight = weight;
        this.isOrganic = isOrganic;
        this.isSugarFree = isSugarFree;
        this.hasNuts = hasNuts;
        this.price = price;
        this.packaging = packaging;
        this.expiryDate = expiryDate;
        this.countryOfOrigin = countryOfOrigin;
        this.cocoaPercentage = cocoaPercentage;
        this.color = color;
        this.isGlutenFree = isGlutenFree;
        this.shape = shape;
        this.isVegan = isVegan;
        this.isDairyFree = isDairyFree;
        this.taste = taste;
        this.calories = calories;
        this.storageInstructions = storageInstructions;
        this.ingredients = ingredients;
        this.manufacturingCompany = manufacturingCompany;
    }
}
